package lacos_condicionais;

public class ControleDeFluxoSwitchCase {

	public static void main(String[] args) {
		
		switch (4) {
		
		case 1: System.out.println("Manhã");
		break;
		
		case 2: System.out.println("Tarde");
		break;
		
		case 3: System.out.println("Noite");
		break;
		
		default: System.out.println("Escolha uma opção válida");
		}

	}

}
